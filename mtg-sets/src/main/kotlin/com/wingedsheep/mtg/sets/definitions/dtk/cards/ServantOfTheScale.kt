package com.wingedsheep.mtg.sets.definitions.dtk.cards

import com.wingedsheep.sdk.dsl.Effects
import com.wingedsheep.sdk.dsl.Targets
import com.wingedsheep.sdk.dsl.Triggers
import com.wingedsheep.sdk.dsl.card
import com.wingedsheep.sdk.model.Rarity
import com.wingedsheep.sdk.scripting.EntersWithCounters
import com.wingedsheep.sdk.scripting.events.CounterTypeFilter
import com.wingedsheep.sdk.scripting.targets.EffectTarget

/**
 * Servant of the Scale
 * {G}
 * Creature — Human Soldier
 * 0/0
 * This creature enters with a +1/+1 counter on it.
 * When this creature dies, put X +1/+1 counters on target creature you control, where X is the
 * number of +1/+1 counters on this creature.
 *
 * The `replacementEffect(EntersWithCounters(...))` line was auto-generated by the mtgish coverage
 * tooling's `AsPermanentEnters` -> `EntersWithACounter` mapping; the dies trigger was hand-wired with
 * the shared MoveAllLastKnownCounters pattern (cf. Dockworker Drone, Broodguard Elite). Since the
 * creature only ever bears +1/+1 counters, moving all last-known counters is equivalent to the
 * printed "X +1/+1 counters" wording.
 */
val ServantOfTheScale = card("Servant of the Scale") {
    manaCost = "{G}"
    colorIdentity = "G"
    typeLine = "Creature — Human Soldier"
    power = 0
    toughness = 0
    oracleText = "This creature enters with a +1/+1 counter on it.\n" +
        "When this creature dies, put X +1/+1 counters on target creature you control, where X is " +
        "the number of +1/+1 counters on this creature."

    replacementEffect(EntersWithCounters(
        counterType = CounterTypeFilter.PlusOnePlusOne,
        count = 1,
        selfOnly = true
    ))

    // When this creature dies, put its +1/+1 counters on target creature you control.
    triggeredAbility {
        trigger = Triggers.Dies
        target = Targets.CreatureYouControl
        effect = Effects.MoveAllLastKnownCounters(EffectTarget.ContextTarget(0))
    }

    metadata {
        rarity = Rarity.COMMON
        collectorNumber = "203"
        artist = "Winona Nelson"
        imageUri = "https://cards.scryfall.io/normal/front/c/a/ca887c41-a8ee-4751-a902-87149c29a9df.jpg?1562793074"
    }
}
