package com.wingedsheep.engine.scenarios

import com.wingedsheep.engine.core.ActivateAbility
import com.wingedsheep.engine.mechanics.layers.StateProjector
import com.wingedsheep.engine.state.components.battlefield.AttachedToComponent
import com.wingedsheep.engine.state.components.stack.ChosenTarget
import com.wingedsheep.engine.support.GameTestDriver
import com.wingedsheep.engine.support.TestCards
import com.wingedsheep.mtg.sets.definitions.mrd.cards.Bonesplitter
import com.wingedsheep.mtg.sets.definitions.mrd.cards.LeoninScimitar
import com.wingedsheep.sdk.core.Step
import com.wingedsheep.sdk.model.Deck
import io.kotest.core.spec.style.FunSpec
import io.kotest.matchers.nulls.shouldBeNull
import io.kotest.matchers.shouldBe

/**
 * Mirrodin Equipment (Bonesplitter, Leonin Scimitar) — auto-generated by the mtgish-tooling emitter
 * once `_Rule = Equip` was mapped (bridge `composed("Equip", ...)` + the emitter `equipAbilityLine`
 * handler that renders the canonical `equipAbility("{cost}")`).
 *
 * These pin the two halves of the generated card end-to-end:
 *  - the `equipAbility(cost)` activated ability attaches the Equipment to a target creature, and
 *  - the no-filter `ModifyStats` (which defaults to `GroupFilter.attachedCreature()`) buffs the
 *    *equipped* creature, surfacing through the projector.
 */
class MirrodinEquipmentTest : FunSpec({

    val projector = StateProjector()

    fun createDriver(): GameTestDriver {
        val driver = GameTestDriver()
        driver.registerCards(TestCards.all + Bonesplitter + LeoninScimitar)
        driver.initMirrorMatch(deck = Deck.of("Forest" to 40), startingLife = 20)
        driver.passPriorityUntil(Step.PRECOMBAT_MAIN)
        return driver
    }

    test("Bonesplitter: equip {1} attaches and gives the equipped creature +2/+0") {
        val driver = createDriver()
        val you = driver.activePlayer!!

        val courser = driver.putCreatureOnBattlefield(you, "Centaur Courser") // 3/3
        val sword = driver.putPermanentOnBattlefield(you, "Bonesplitter")
        val equipId = Bonesplitter.activatedAbilities.first().id

        // Unequipped: base 3/3.
        projector.getProjectedPower(driver.state, courser) shouldBe 3
        projector.getProjectedToughness(driver.state, courser) shouldBe 3

        driver.giveColorlessMana(you, 1)
        driver.submit(
            ActivateAbility(you, sword, equipId, targets = listOf(ChosenTarget.Permanent(courser)))
        ).isSuccess shouldBe true
        driver.bothPass()

        driver.state.getEntity(sword)?.get<AttachedToComponent>()?.targetId shouldBe courser
        // +2/+0 on the equipped creature.
        projector.getProjectedPower(driver.state, courser) shouldBe 5
        projector.getProjectedToughness(driver.state, courser) shouldBe 3
    }

    test("Leonin Scimitar: equip {1} attaches and gives the equipped creature +1/+1") {
        val driver = createDriver()
        val you = driver.activePlayer!!

        val courser = driver.putCreatureOnBattlefield(you, "Centaur Courser") // 3/3
        val scimitar = driver.putPermanentOnBattlefield(you, "Leonin Scimitar")
        val equipId = LeoninScimitar.activatedAbilities.first().id

        // Not enough mana — the equip fails and nothing attaches.
        driver.submitExpectFailure(
            ActivateAbility(you, scimitar, equipId, targets = listOf(ChosenTarget.Permanent(courser)))
        )
        driver.state.getEntity(scimitar)?.get<AttachedToComponent>().shouldBeNull()

        driver.giveColorlessMana(you, 1)
        driver.submit(
            ActivateAbility(you, scimitar, equipId, targets = listOf(ChosenTarget.Permanent(courser)))
        ).isSuccess shouldBe true
        driver.bothPass()

        driver.state.getEntity(scimitar)?.get<AttachedToComponent>()?.targetId shouldBe courser
        // +1/+1 on the equipped creature.
        projector.getProjectedPower(driver.state, courser) shouldBe 4
        projector.getProjectedToughness(driver.state, courser) shouldBe 4
    }
})
